using System;

namespace Universe.Framework.ConsoleApp.Tests.Algoritms
{
    /// <summary>
    /// <author>Alex Envision</author>
    /// </summary>
    public class MathAlgTest
    {
        public void Run()
        {
            var power = pow(2.78, 0.38);

            var powerBase = Math.Pow(2.78, 0.38);

            var eqv = Math.Abs(power - powerBase) < 0.00001;

            Console.ReadLine();
        }

        public double pow(double a, double b)
        {
            if (b == 0)
                return 1;
            else if (b < 0)
            {
                return 1 / pow(a, -1 * b);
            }
            else if ((int)b % 2 == 0)
            {
                double h = pow(a, (int)(b / 2));
                return h * h;
            }
            else
            {
                return a * pow(a, b - 1);
            }
        }
    }
}
