using System;
using System.Data.Entity;
using System.Linq;
using System.Reflection;
using Universe.DataAccess.TableContent.Framework;

namespace Universe.DataAccess.TableContent.Config
{
    /// <summary>
    ///     Конфигуратор сидов данных.
    /// <author>Alex Envision</author>
    /// </summary>
    public class SeedingEntitiesConfig
    {
        public static void Configure<TUniverseDbContext>(TUniverseDbContext context) where TUniverseDbContext: DbContext, IUniverseDbContext
        {
            var fillers = typeof(TUniverseDbContext).Assembly
                .GetTypes()
                .Where(x => !x.IsAbstract && x.GetInterfaces().Contains(typeof(IEntitySeeder)))
                .OrderBy(x => x.GetCustomAttribute<OrderAttribute>()?.Order ?? 0)
                .Select(Activator.CreateInstance)
                .Cast<IEntitySeeder>().ToList();

            foreach (var filler in fillers)
            {
                Console.WriteLine(filler.GetType().Name);
                filler.Apply(context);
            }
        }
    }
}