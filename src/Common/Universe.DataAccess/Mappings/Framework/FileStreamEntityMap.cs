using System.ComponentModel.DataAnnotations.Schema;
using System.Data.Entity;
using System.Data.Entity.ModelConfiguration;
using Universe.DataAccess.Models;

namespace Universe.DataAccess.Mappings.Framework
{
    internal abstract class FileStreamEntityMap<T> : IFileStreamEntityMap
        where T : FileStreamEntity
    {
        public virtual void Apply(DbModelBuilder builder)
        {
            var configuration = builder.Entity<T>();
            configuration.Property(x => x.FileContentId).HasDatabaseGeneratedOption(DatabaseGeneratedOption.Identity);
            configuration.HasKey(x => x.FileContentId);

            configuration.ToTable(typeof(T).Name, "dbo");
            Map(configuration);
        }

        protected abstract void Map(EntityTypeConfiguration<T> configuration);
    }
}