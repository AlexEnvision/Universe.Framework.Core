using System;
using System.Data.Entity;
using System.Threading.Tasks;

namespace Universe.CQRS.Infrastructure
{
    /// <summary>
    /// <author>Alex Envision</author>
    /// </summary>
    public class UnitOfWork
    {
        private readonly DbContext _dbCtx;

        public UnitOfWork(DbContext dbCtx)
        {
            _dbCtx = dbCtx ?? throw new ArgumentNullException(nameof(dbCtx));
        }

        public async Task<T> ExecAsync<T>(Func<DbContext, DbContextTransaction, Task<T>> commands)
        {
            using (var transaction = _dbCtx.Database.BeginTransaction())
            {
                var result = await commands(_dbCtx, transaction);
                await _dbCtx.SaveChangesAsync();
                transaction.Commit();
                transaction.Dispose();            
                return result;
            }
        }

        public T Exec<T>(Func<DbContext, DbContextTransaction, T> commands)
        {
            using (var transaction = _dbCtx.Database.BeginTransaction())
            {
                var result = commands(_dbCtx, transaction);
                _dbCtx.SaveChanges();
                transaction.Commit();
                transaction.Dispose();
                return result;
            }
        }
    }
}