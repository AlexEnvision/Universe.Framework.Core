using Newtonsoft.Json;
using Newtonsoft.Json.Serialization;

namespace Universe.CQRS.Infrastructure.Json
{
    /// <summary>
    /// <author>Alex Envision</author>
    /// </summary>
    public sealed class UniverseModelDeserializer
    {
        private readonly DynamicModelMapper _dynamicModelMapper = new DynamicModelMapper();

        public T Deserialize<T>(string json) where T : class, new()
        {
            var model = JsonConvert.DeserializeObject<dynamic>(
                json,
                new JsonSerializerSettings {
                    ContractResolver = new DefaultContractResolver {
                        NamingStrategy = new CamelCaseNamingStrategy()
                    },
                    DateParseHandling = DateParseHandling.DateTimeOffset,
                    DateFormatHandling = DateFormatHandling.IsoDateFormat,
                    DateTimeZoneHandling = DateTimeZoneHandling.Local
                });

            return (T)_dynamicModelMapper.Map<T>(model);
        }
    }
}