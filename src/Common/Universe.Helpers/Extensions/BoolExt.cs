        }

        public static bool IsNotTrue(this bool? v)
        {
            return !v.IsTrue();
        }

        public static bool IsNull(this bool? v)
        {
            return v == null;
        }

        public static bool IsTrue(this bool? v)
        {
            return v ?? false;
        }
