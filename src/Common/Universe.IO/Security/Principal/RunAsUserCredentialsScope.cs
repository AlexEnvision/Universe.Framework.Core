using System;
using Universe.IO.Security.Principal.Impersonation;

namespace Universe.IO.Security.Principal
{
    public class RunAsUserCredentialsScope
    {
        private readonly string _domain;
        private readonly string _username;
        private readonly string _password;
        private readonly LogonType _logonType;

        public RunAsUserCredentialsScope(string domain, string username, string password, LogonType logonType)
        {
            _domain = domain;
            _username = username;
            _password = password;
            _logonType = logonType;
        }

        public void RunAsUser(Action action)
        {
            var credentials = new UserCredentials(_domain, _username, _password);
            ApplyImpersonation.RunAsUser(credentials, _logonType, action);
        }
    }
}