using System;
using System.Net;
using Universe.IO.DownloadClient.Interfaces;

namespace Universe.IO.DownloadClient.Ftp
{
    /// <summary>
    ///     FTP-клиент
    /// <author>Alex Envision</author>
    /// </summary>
    public class FtpClient : IDownloadClient
    {
        public FtpClient(Uri host)
        {
            if (host == null)
                throw new ArgumentNullException(nameof(host));

            if (host.Scheme != Uri.UriSchemeFtp)
                throw new NotSupportedException("Указанная схема url не поддерживается/ The specified url schema is not supported");

            Host = host;
        }

        public ICredentials Credentials { get; set; }

        public bool EnableSsl { get; set; }

        public Uri Host { get; set; }

        public IDownloadFolder GetRootFolder()
        {
            return new FtpFolder(this, null);
        }
    }
}