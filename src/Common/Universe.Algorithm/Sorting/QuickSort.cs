namespace Universe.Algorithm.Sorting
{
    /// <summary>
    /// <author>Alex Envision</author>
    /// </summary>
    internal class QuickSort : BaseSortingAlgorithm
    {
        public override T[] Sorting<T>(T[] array)
        {
            Sort(array, 0, array.Length - 1);
            return array;
        }

        private void Sort<T>(T[] array, int l, int r)
        {
            dynamic x = array[l + (r - l) / 2];
            //запись эквивалентна (l+r)/2,
            //но не вызывает переполнения на больших данных
            var i = l;
            var j = r;
            //код в while обычно выносят в процедуру particle
            while (i <= j)
            {
                while (array[i] < x) i++;
                while (array[j] > x) j--;
                if (i <= j)
                {
                    Swap(array, i, j);
                    i++;
                    j--;
                }
            }
            if (i < r)
                Sort(array, i, r);
            if (l < j)
                Sort(array, l, j);
        }
    }
}