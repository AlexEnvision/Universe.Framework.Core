namespace Universe.Algorithm.Sorting
{
    /// <summary>
    /// <author>Alex Envision</author>
    /// </summary>
    internal class ShakerSort : BaseSortingAlgorithm
    {
        public override T[] Sorting<T>(T[] array)
        {
            int left = 0,
                right = array.Length - 1,
                count = 0;

            while (left <= right)
            {
                for (var i = left; i < right; i++)
                {
                    count++;
                    dynamic x = array[i + 1];
                    if (array[i] > x)
                        Swap(array, i, i + 1);
                }
                right--;

                for (var i = right; i > left; i--)
                {
                    count++;
                    dynamic x = array[i];
                    if (array[i - 1] > x)
                        Swap(array, i - 1, i);
                }
                left++;
            }
            return array;
        }
    }
}