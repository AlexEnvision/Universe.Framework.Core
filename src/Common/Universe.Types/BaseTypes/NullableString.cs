namespace Universe.Types.BaseTypes
{
    /// <summary>
    /// <author>Alex Envision</author>
    /// </summary>
    public class NullableString
    {
        private readonly string _innerString;

        public string Value => _innerString;

        public NullableString(string str)
        {
            _innerString = str;
        }

        public static NullableString FromString(string str)
        {
            return new NullableString(str);
        }

        // Подменяем оператор ?? перегруженным оператором %
        public static NullableString operator %(NullableString str1, NullableString str2)
        {
            if (str1?.IsNullOrEmpty() ?? true)
            {
                if (str2?.IsNullOrEmpty() ?? true)
                {
                    return null;
                }
                else
                {
                    return str2;
                }
            }
            else
            {
                return str1;
            }
        }

        public override string ToString()
        {
            return Value;
        }
    }
}
